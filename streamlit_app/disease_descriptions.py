"""
Descripciones de las enfermedades de las hojas de limón.
"""

DISEASE_DESCRIPTIONS = {
    "Anthracnose": 
    """
    Enfermedad fúngica que causa manchas oscuras y hundidas en las hojas. 
    Los síntomas incluyen lesiones necróticas de color marrón oscuro que pueden 
    expandirse y causar la caída prematura de las hojas. Afecta principalmente 
    durante períodos de alta humedad y temperaturas cálidas.
    """,
    
    "Bacterial Blight": 
    """
    Enfermedad bacteriana que produce manchas acuosas en las hojas que se vuelven 
    necróticas. Las lesiones suelen tener un halo amarillento y pueden causar 
    defoliación severa. Se propaga fácilmente por salpicaduras de agua y 
    herramientas contaminadas.
    """,
    
    "Citrus Canker": 
    """
    Enfermedad bacteriana altamente contagiosa que forma lesiones elevadas 
    en las hojas, frutos y tallos. Las manchas tienen un aspecto corchoso 
    con un halo amarillento. Puede causar defoliación severa y afectar 
    significativamente la producción.
    """,
    
    "Curl Virus": 
    """
    Enfermedad viral que causa el enrollamiento y distorsión de las hojas. 
    Las hojas afectadas muestran clorosis y pueden presentar un aspecto 
    arrugado. Se transmite principalmente por insectos vectores y puede 
    afectar el crecimiento de la planta.
    """,
    
    "Deficiency Leaf": 
    """
    Condición que muestra síntomas de deficiencia nutricional, como clorosis 
    (amarillamiento) entre las venas, manchas necróticas o bordes quemados. 
    Puede ser causada por falta de nitrógeno, hierro, magnesio u otros 
    nutrientes esenciales.
    """,
    
    "Dry Leaf": 
    """
    Condición donde las hojas muestran signos de deshidratación y estrés 
    hídrico. Las hojas pueden aparecer marchitas, quebradizas y con bordes 
    secos. Puede ser causada por falta de riego, daño por sal o condiciones 
    ambientales extremas.
    """,
    
    "Healthy Leaf": 
    """
    Hoja que muestra un color verde uniforme, sin manchas ni deformaciones. 
    Presenta un aspecto turgente, con venas bien definidas y sin signos de 
    estrés o enfermedad. Es el estado ideal para el crecimiento y desarrollo 
    de la planta.
    """,
    
    "Sooty Mould": 
    """
    Hongo que crece sobre la melaza excretada por insectos como pulgones o 
    moscas blancas. Forma una capa negra sobre las hojas que puede afectar 
    la fotosíntesis. No es una enfermedad directa pero indica la presencia 
    de plagas de insectos.
    """,
    
    "Spider Mites": 
    """
    Plaga microscópica que causa daño al alimentarse de las hojas. Los síntomas 
    incluyen punteado fino, decoloración y telarañas en el envés de las hojas. 
    Pueden causar defoliación severa en infestaciones fuertes y se desarrollan 
    mejor en condiciones cálidas y secas.
    """
} 